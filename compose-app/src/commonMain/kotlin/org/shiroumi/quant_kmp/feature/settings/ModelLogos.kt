private fun buildLogo(name: String, pathData: String): ImageVector =
    ImageVector.Builder(
        name = name,
        defaultWidth = 24.dp,
        defaultHeight = 24.dp,
        viewportWidth = 24f,
        viewportHeight = 24f,
    ).addPath(
        pathData = addPathNodes(pathData),
        fill = SolidColor(Color.Black),
    ).build()

val DeepSeekLogo: ImageVector by lazy { buildLogo("DeepSeek", DEEPSEEK_PATH) }
val MoonshotLogo: ImageVector by lazy { buildLogo("MoonshotAI", MOONSHOT_PATH) }

/**
 * 根据预设元信息推断对应的厂商 logo；未命中返回 null。
 */
fun AgentModelPresetDto.logoVector(): ImageVector? {
    val matchText = "$key $provider $modelId $displayName".lowercase()
    return when {
        "deepseek" in matchText -> DeepSeekLogo
        "kimi" in matchText || "moonshot" in matchText -> MoonshotLogo
        else -> null
    }
}
