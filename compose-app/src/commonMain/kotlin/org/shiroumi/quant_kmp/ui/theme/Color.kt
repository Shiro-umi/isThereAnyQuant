    AppColorTheme.WarmBrownRed -> if (useDarkColors) warmBrownRedDark else warmBrownRedLight
    AppColorTheme.IndigoBlue -> if (useDarkColors) indigoBlueDark else indigoBlueLight
    AppColorTheme.TealGreen -> if (useDarkColors) tealGreenDark else tealGreenLight
    AppColorTheme.VioletPurple -> if (useDarkColors) violetPurpleDark else violetPurpleLight
    AppColorTheme.ForestGreen -> if (useDarkColors) forestGreenDark else forestGreenLight
    AppColorTheme.OceanBlue -> if (useDarkColors) oceanBlueDark else oceanBlueLight
    AppColorTheme.AmberGold -> if (useDarkColors) amberGoldDark else amberGoldLight
    AppColorTheme.RoseRed -> if (useDarkColors) roseRedDark else roseRedLight
    AppColorTheme.GraphiteGray -> if (useDarkColors) graphiteGrayDark else graphiteGrayLight
    AppColorTheme.SkyBlue -> if (useDarkColors) skyBlueDark else skyBlueLight
    AppColorTheme.MintCyan -> if (useDarkColors) mintCyanDark else mintCyanLight
    AppColorTheme.CoralOrange -> if (useDarkColors) coralOrangeDark else coralOrangeLight
    AppColorTheme.PlumPurple -> if (useDarkColors) plumPurpleDark else plumPurpleLight
    AppColorTheme.SteelBlueGray -> if (useDarkColors) steelBlueGrayDark else steelBlueGrayLight
